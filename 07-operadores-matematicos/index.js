//Operações matemáticas

{
  const primeiroValor = 5;
  const segundoValor = '2';

  console.log(primeiroValor + segundoValor);
  //atenção! tratando-se de dois tipos de dados diferentes, as variáveis serão concatenadas, resultando em "52".
}

{
  const primeiroValor = 5;
  const segundoValor = 2;

  console.log(primeiroValor + segundoValor); // resultado: 7 
  console.log(primeiroValor - segundoValor); // resultado: 3
  console.log(primeiroValor * segundoValor); // resultado: 10
  console.log(primeiroValor / segundoValor); // resultado: 2.5
  console.log(primeiroValor % segundoValor); // resultado: 1
}   
